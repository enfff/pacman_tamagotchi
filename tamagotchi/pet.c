void pet_animation_snack(int x_offset, int y_offset){
	// disegna la fragola a partire dalla posizione (fissa) 202, 192
	// disegna pacman che si sposta verso destra di 30 pixel un totale di 3 volte
	// ad ogni iterazione ricopre di nero il passo precedente
	//x_offset, y_offset devono essere la posizione iniziale del pet
	int i,j,k;
	
	for(i=0; i<SNACKSIZE; i++){
		for(j=0; j<SNACKSIZE; j++){	
			uint16_t PixelColorImDrawing = pet_snack_stance1[i*SNACKSIZE + j];
			if(PixelColorImDrawing != White){
				LCD_SetPoint(j + 202, i + 192, PixelColorImDrawing);
			}
		}
	}
	
	for(k=1; k<4; k++){
		for(i=0; i<PETSIZE; i++){
			for(j=0; j<PETSIZE; j++){
				
				uint16_t PixelColorImDrawing;
				
				switch(k){
					case 1: case 3:
						PixelColorImDrawing = pet_stance2[i*PETSIZE + j];
						break;
					default:
						PixelColorImDrawing = pet_stance1[i*PETSIZE + j];
				}
				
				if(PixelColorImDrawing != Black){
					LCD_SetPoint(y_offset + j + k*PETSIZE, x_offset + i, PixelColorImDrawing);
				}
				
				LCD_SetPoint(y_offset + j + (k-1)*PETSIZE, x_offset + i, Black);						// cancella il frame precedente
			}
		}
	}
	
	for(i=0; i<PETSIZE; i++){
		for(j=0; j<PETSIZE; j++){
			LCD_SetPoint(y_offset + j + 4*PETSIZE, x_offset + i, Black);						// cancella l'ultimo frame
		}
	}
	
}


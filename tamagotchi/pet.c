void draw_something(int x_offset, int y_offset, uint16_t* pixel_array, int SIZE){
	int i,j;
	
	for(i=0; i<SIZE; i++){
		for(j=0; j<SIZE; j++){	
			uint16_t PixelColorImDrawing = pixel_array[i*SIZE + j];
			LCD_SetPoint(y_offset + j, x_offset + i, PixelColorImDrawing);
		}
	}
}

